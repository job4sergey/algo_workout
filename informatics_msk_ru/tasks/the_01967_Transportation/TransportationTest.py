import os

os.environ['NOMAIN'] = '1'
from com.mcc.tasks.the_01967_Transportation.MccSolution import \
    solution

from com.letcode.common.TestCaseBase import TestCaseBase


class TaskCSaturdayTest(TestCaseBase):
    def atest(self, N, es, e):
        self.assertEquals(solution(N, es), e)

    def test(self):
        atest = self.atest
        s2a = self.s2a

        atest(7, [s2a('1 2 700 3001001'), s2a('2 4 700 3000801'),
                  s2a('4 6 40 3000601'), s2a('6 7 0 3000801'),
                  s2a('1 3 10 3000401'), s2a('3 5 10 3000601'),
                  s2a('5 6 10 3000401')], 6)

        atest(3, [s2a('2 1 10 3000220'), s2a('2 3 20 3000201'), s2a('3 1 0 3000401')], 4)

        atest(2, [s2a('1 2 1441 3000220')], 0)
        atest(2, [s2a('1 2 10 3000220')], 2)
        atest(2, [s2a('1 2 1440 3000220')], 2)

        atest(7, [s2a('1 2 700 3001001'), s2a('2 4 700 3000801'),
                  s2a('4 6 40 3000801'), s2a('6 7 0 3000801'),
                  s2a('1 3 10 3000401'), s2a('3 5 10 3000601'),
                  s2a('5 6 10 3000401')], 8)

        atest(7, [s2a('1 2 700 3001001'), s2a('2 4 700 3000801'),
                  s2a('4 6 41 3000801'), s2a('6 7 0 3000801'),
                  s2a('1 3 10 3000401'), s2a('3 5 10 3000601'),
                  s2a('5 6 10 3000401')], 4)

        atest(3, [s2a('1 2 10 3000220'), s2a('2 3 20 3000201'), s2a('1 3 0 3000401')], 4)
        atest(3, [s2a('1 2 10 3000220'), s2a('2 3 20 3000201'), s2a('1 3 1 3000099')], 2)
        atest(3, [s2a('1 2 10 3000099'), s2a('2 3 20 3000099'), s2a('1 3 1 3000099')], 0)
        atest(1, [s2a('1 1 10 3000220')], 10000000)
        atest(1, [], 10000000)
        atest(3, [s2a('1 2 10 3000220'), s2a('2 3 20 3000201'), s2a('1 3 1 3000399')], 3)
        atest(3, [s2a('1 2 10 3000220'), s2a('2 3 20 3000201'), s2a('1 3 1440 3000399')], 3)
        atest(3, [s2a('1 2 10 3000220'), s2a('2 3 20 3000201'), s2a('1 3 1441 3000399')], 2)
        atest(2, [s2a('1 2 10 3000220')], 2)
